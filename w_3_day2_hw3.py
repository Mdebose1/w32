import math
# Convert the list below from Celsius to Farhenheit, using the map function with a lambda
# HINT: KEEP IN MIND PEMDAS. USE THE CELCIUS - FAHRENHEIT CONVERSION\n",
    # "# F = C * 9/5 + 32\n"
celsius = int(input("What are the degrees in celsius  ?"))
fahrenheit = float(celsius*1.8)+32
places = {'Nassau': 32, 'Boston': 12, 'Los Angeles': 44, 'Miami': 29}
for v in places (map(lambda x: x , fahrenheit)):
    print(v)


